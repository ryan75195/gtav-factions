namespace FactionWars.UI.Models
{
    /// <summary>
    /// Defines the types of notifications that can be displayed.
    /// </summary>
    public enum NotificationType
    {
        /// <summary>
        /// Informational notification.
        /// </summary>
        Info = 0,

        /// <summary>
        /// Success notification indicating a positive outcome.
        /// </summary>
        Success = 1,

        /// <summary>
        /// Warning notification indicating potential issues.
        /// </summary>
        Warning = 2,

        /// <summary>
        /// Error notification indicating a problem.
        /// </summary>
        Error = 3
    }
}
